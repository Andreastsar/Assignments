package models;

/**
 *
 * @author mac
 */
public class Door {
    private String name;

    public Door() {
    }

    public Door(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Door{" +
                "name='" + name + '\'' +
                '}';
    }
}
